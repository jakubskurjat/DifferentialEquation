public class Point {

    private double t;
    private double x;
    private double v;

    public Point(double t, double x, double v) {
        this.t = t;
        this.x = x;
        this.v = v;
    }

    public double getT() {
        return t;
    }

    public double getX() {
        return x;
    }

    public double getV() {
        return v;
    }
}
